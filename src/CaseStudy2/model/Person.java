package CaseStudy2.model;

public abstract class Person {

    private String name, gender, dob, email, id, mail;
    private int  phoneNumber, idCard;

    public Person() {
    }

    public Person(String name, String gender, String dob, String email, String id, String mail, int phoneNumber, int idCard) {
        this.name = name;
        this.gender = gender;
        this.dob = dob;
        this.email = email;
        this.id = id;
        this.mail = mail;
        this.phoneNumber = phoneNumber;
        this.idCard = idCard;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public int getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(int phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public int getIdCard() {
        return idCard;
    }

    public void setIdCard(int idCard) {
        this.idCard = idCard;
    }
}
